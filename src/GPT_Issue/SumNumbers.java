package GPT_Issue;
import java.util.Scanner;
/*
Напишите программу, которая запрашивает у пользователя целое положительное число
и затем вычисляет сумму всех его цифр.
Программа должна проверять, что введенное значение является целым числом и больше либо равно нулю.

Пример ввода и вывода:

Введите целое положительное число: 12345
Сумма цифр: 15

Введите целое положительное число: -123
Ошибка! Введите положительное число.
 */
public class SumNumbers {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        System.out.println("Введите целое положительное число: ");

        if (scanner.hasNextInt()) {          // Проверяем, что введенное значение - целое число
            int number = scanner.nextInt();

            if (number >= 0) {               // Проверяем, что число положительное
                int sum = 0;

                while (number > 0) {         // Вычисляем сумму цифр
                    int digit = number % 10; // Получаем последнюю цифру числа
                    sum += digit;            // Добавляем цифру к сумме
                    number /= 10;            // Убираем последнюю цифру из числа
                }

                System.out.println("Сумма цифр: " + sum);
            } else {
                System.out.println("Ошибка! Введите положительное число.");
            }
        } else {
            System.out.println("Ошибка! Введите целое число.");
        }
    }
}
